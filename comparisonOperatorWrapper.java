class comparisonOperatorsWrapper
{
	public static void main(String args[])
	{
		Integer i=10;
		Integer j=10;
		
		String s1="Hello";
		String s2="Hii";
	
		System.out.println("i > j");
		System.out.println(i > j);
		
		System.out.println("i == j");
		System.out.println(i == j);

		System.out.println("s1 == s2");
		System.out.println(s1 == s2);

		System.out.println("s1 != s2");
		System.out.println(s1 != s2);
	}
}
	