class arithmeticOperators
{
	
	public static void main(String args[])
	{
		int a=10;
		int b=3;
		int result;

		result = a + b;
		System.out.println("a + b = "+result);

		result = a - b;
		System.out.println("a - b = "+result);
		
		result = a * b;
		System.out.println("a * b = "+result);
		
		result = a / b;
		System.out.println("a / b = "+result);
		
		result = a % b;
		System.out.println("a % b = "+result);
	}
}
	